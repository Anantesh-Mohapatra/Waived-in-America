def anchored_panel_path(variant: str = "main") -> Path:
    """Anchored panel parquet for a panel variant. Filenames preserved from the
    original pipeline (anchored_panel.parquet / anchored_panel_dla_only.parquet)."""
    return ANCHORED / f"anchored_panel{variant_suffix(variant)}.parquet"


def donor_universe_path(variant: str = "main") -> Path:
    """Donor universe CSV, rebuilt each run by build_donor_universe.py."""
    return DATA / f"donor_universe{variant_suffix(variant)}.csv"


def cache_dir(variant: str = "main") -> Path:
    return CACHE / variant
