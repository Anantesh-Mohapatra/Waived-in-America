def variant_suffix(variant: str) -> str:
    """The filename-suffix rule: main is unsuffixed, every other variant is
    `_<variant>`. Single owner of the rule (mirrored in pipeline/lib/paths.R
    for the R scripts, which cannot import this module)."""
    return "" if variant == "main" else f"_{variant}"


def results_root(variant: str = "main") -> Path:
    """Committed results tree for a variant (main, dla_only, uniform_sample, non_container)."""
    return RESULTS / variant


# ---- results-layout functions (single owner; pipeline/lib/paths.R mirrors
# ---- these for the R scripts) ------------------------------------------------
def panel_logs(variant: str = "main") -> Path:
    return results_root(variant) / "panels"


