"""Central path registry for the entire pipeline.

Every pipeline script resolves paths through this module — the repo has exactly
ONE root anchor. Standard header for any script under pipeline/<stage>/:

    import sys
    from pathlib import Path
    sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "lib"))
    from paths import REPO_ROOT, CLEAN, ...

R scripts carry an identical inline `find_root()` block (WIA_ROOT env
override, else walk up to pyproject.toml) — inlined because an R script
cannot source a shared helper before knowing the root.
"""

